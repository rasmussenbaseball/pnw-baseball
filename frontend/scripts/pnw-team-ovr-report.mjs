/**
 * PNW Team OVR report — runs each level multiple times and averages the
 * starting Team OVR for every PNW program. This is the SAME number the
 * Roster / Dashboard pages show under "Team OVR" — top-9 hitters × 0.55 +
 * top-5 pitchers × 0.45.
 *
 * Run with: cd frontend && npx tsx scripts/pnw-team-ovr-report.mjs
 *
 * RNG is per-school-per-run, so individual runs vary ±1-2 OVR. Averaging
 * across 8 runs gives a stable starting Team OVR for the canonical hierarchy.
 */

import { newDynasty } from '../src/gm/engine/newDynasty.js'
import { newDynastyMultiLevel } from '../src/gm/engine/newDynastyMultiLevel.js'
import { pnwProgramsAtLevel } from '../src/gm/engine/pnwPlayoffs.js'
import { teamOverall } from '../src/gm/engine/playerRating.js'

const RUNS = 8
const LEVELS = ['D1', 'D2', 'D3', 'NAIA', 'NWAC']

function baseInput(suffix = '') {
  return {
    userSupabaseId: 'ovrrep-' + suffix,
    saveSlot: 1,
    dynastyName: 'OVR Report',
    gameOptions: {
      mode: 'TRADITIONAL', storyMode: 'REGULAR', difficulty: 'NORMAL',
      injuriesEnabled: false, coachFiringEnabled: false,
      transferPortalEnabled: false, budgetConstraintsEnabled: false,
    },
    userCoach: {
      firstName: 'Rep', lastName: 'Tester',
      lookId: 0, primaryRegion: 'NW', secondaryRegion: 'W',
      regions: ['NW', 'W'], archetype: 'GENERALIST',
      recruiter_type: 'BALANCED',
      developer: 50, motivator: 50, recruiter: 50, tactician: 50,
    },
  }
}

function makeState(level, schoolId, conferenceId, runIdx) {
  const input = { ...baseInput('r' + runIdx), userSchoolId: schoolId }
  if (level === 'NAIA') return newDynasty(input)
  return newDynastyMultiLevel({ ...input, level, conferenceId })
}

const samples = {} // levelTeamId → [Team OVR for each run]

for (const level of LEVELS) {
  const programs = pnwProgramsAtLevel(level)
  for (let r = 0; r < RUNS; r++) {
    // One dynasty per conference per run gives us all teams in that conf's
    // rosters (the league is generated by newDynasty(MultiLevel)).
    const seen = new Set()
    for (const p of programs) {
      const conf = p.conferenceId
      if (seen.has(conf)) continue
      seen.add(conf)
      let state
      try {
        state = makeState(level, p.id, conf, r)
      } catch (e) {
        continue
      }
      // Capture Team OVR for every PNW team that has a roster in this state
      for (const pp of programs) {
        const team = state.teams[pp.id]
        if (!team) continue
        const t = teamOverall(team, state.players)
        const k = `${level}::${pp.id}`
        if (!samples[k]) samples[k] = []
        samples[k].push(t.overall)
      }
    }
  }
}

console.log('═══════════════════════════════════════════════════════════════')
console.log(`PNW STARTING TEAM OVR — averaged over ${RUNS} dynasty runs per level`)
console.log('(Same number the Roster page shows under "Team OVR")')
console.log('═══════════════════════════════════════════════════════════════\n')

// Flat list, sorted by avg Team OVR within each level
for (const level of LEVELS) {
  const programs = pnwProgramsAtLevel(level)
  const rows = programs.map(p => {
    const k = `${level}::${p.id}`
    const arr = samples[k] || []
    if (arr.length === 0) return { name: p.name, avg: null, min: null, max: null, n: 0 }
    const avg = Math.round(arr.reduce((s, x) => s + x, 0) / arr.length)
    return {
      name: p.name,
      avg,
      min: Math.min(...arr),
      max: Math.max(...arr),
      n: arr.length,
    }
  }).sort((a, b) => (b.avg ?? -1) - (a.avg ?? -1))

  console.log(`──── ${level} ─────────────────────────────────────────────`)
  for (const r of rows) {
    if (r.avg == null) {
      console.log(`  ${r.name.padEnd(28)} (no data)`)
    } else {
      const range = r.max === r.min ? '' : ` (range ${r.min}-${r.max})`
      console.log(`  ${r.name.padEnd(28)} Team OVR ${String(r.avg).padStart(3)}${range}`)
    }
  }
  console.log()
}
